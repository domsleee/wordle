function getOddEven(myArr) {
    let oddEven = myArr.map(t => parseInt(myArr)%2 == 1 ? 'O' : 'E');
    return oddEven.sort().join('');
}

let theCache = {}, callCt = 0;
function canOddBeReducedCached(oddEven1, oddEven2) {
    if (oddEven1 in theCache) {
        if (oddEven2 in theCache[oddEven1]) {
            return theCache[oddEven1][oddEven2];
        }
    } else theCache[oddEven1] = {};
    callCt++;
    return theCache[oddEven1][oddEven2] = canOddBeReduced(oddEven1, oddEven2);
}

function canOddBeReduced(oddEven1, oddEven2) {
    if (oddEven1.length < oddEven2.length) return false;
    if (oddEven1 == oddEven2) return true;
    //console.log(`can ${oddEven1} be reduced to ${oddEven2}`)

    // oddEven1 - can delete Os until O matches oddEven2
    // EOO --> EO
    // EOO --> EE
    for (let i = 0; i < oddEven1.length; ++i) {
        for (let j = i+1; j < oddEven1.length; ++j) {
            let oCt = oddEven1[i] == 'O' + oddEven1[j] == 'O';
            const nextStr = oddEven1.slice(0, i) + oddEven1.slice(i+1, j) + oddEven1.slice(j+1) + (oCt == 1 ? 'O' : 'E');
            const nextArr = nextStr.split('').sort().join('');
            if (canOddBeReduced(nextArr, oddEven2)) return true;
        }
    }
    return false;
}

